object PassportData {
    val stamps = mutableStateListOf<PassportStamp>()
    var earnedSwipes: Int = 0

    fun addStamp(stamp: PassportStamp, context: Context) {
        stamps.add(0, stamp)
        earnedSwipes += 3
        LearningManager.addBonusSwipes(context, 3)
    }
}

